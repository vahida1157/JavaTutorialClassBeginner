package Example1.Classes;

public class Animal {
    protected char gender;
    protected String color;
    protected String race;
    protected int age;

    public Animal() {
    }

    public Animal(char gender, String color, String race, int age) {
        this.gender = gender;
        this.color = color;
        this.race = race;
        this.age = age;
    }
}
